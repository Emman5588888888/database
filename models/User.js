const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    min: 2,
    max: 50,
    required: true,
  },
  lastName: {
    type: String,
    min: 2,
    max: 50,
    required: true,
  },
  phone: {
    type: String,
    max: 50,
    required: true,
  },
  city: {
    type: String,
    min: 2,
    max: 50,
    required: true,
  },
  country: {
    type: String,
    min: 2,
    max: 50,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    min: 2,
    max: 30,
    required: true,
  },
  password: {
    type: String,
    max: 255,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

